function buscarEstudiantePorRUT(rut) {
    const rutLimpio = rut.trim().toUpperCase();
    
    for (const curso in estudiantesPorCurso) {
        const estudiantes = estudiantesPorCurso[curso];
        const encontrado = estudiantes.find(e => e.rut.trim().toUpperCase() === rutLimpio);
        if (encontrado) {
            return {
                ...encontrado,
                curso: curso
            };
        }
    }
    return null;
}

function buscarEstudiantePorNombre(nombre) {
    const nombreLimpio = nombre.trim().toLowerCase();
    
    for (const curso in estudiantesPorCurso) {
        const estudiantes = estudiantesPorCurso[curso];
        const encontrado = estudiantes.find(e => 
            e.nombre.toLowerCase().includes(nombreLimpio)
        );
        if (encontrado) {
            return {
                ...encontrado,
                curso: curso
            };
        }
    }
    return null;
}

function obtenerTodosLosEstudiantes() {
    const todos = [];
    for (const curso in estudiantesPorCurso) {
        estudiantesPorCurso[curso].forEach(e => {
            todos.push({
                ...e,
                curso: curso
            });
        });
    }
    return todos;
}

