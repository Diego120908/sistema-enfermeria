function verCurso(curso) {
    const estudiantes = estudiantesPorCurso[curso];
    if (estudiantes) {
        console.log(`📚 ${curso} - ${estudiantes.length} estudiantes:`);
        estudiantes.forEach(e => {
            console.log(`  - ${e.nombre}: ${e.rut}`);
        });
        return estudiantes;
    } else {
        console.log(`❌ Curso "${curso}" no encontrado`);
        return null;
    }
}

function verTodosLosCursos() {
    console.log("📚 TODOS LOS CURSOS:");
    for (const curso in estudiantesPorCurso) {
        console.log(`\n${curso}: ${estudiantesPorCurso[curso].length} estudiantes`);
    }
    return Object.keys(estudiantesPorCurso);
}

function totalEstudiantes() {
    let total = 0;
    for (const curso in estudiantesPorCurso) {
        total += estudiantesPorCurso[curso].length;
    }
    console.log(`👥 Total de estudiantes: ${total}`);
    return total;
}